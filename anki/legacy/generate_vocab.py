HEADER = "# 初级1-2 单词表（大家的日语）\n\n"
TABLE_HEADER = "| 日语 | 读音 | 词性 | 中文 |\n|------|------|------|------|\n"

def build_markdown() -> str:
    parts = [HEADER]
    for lesson_num in sorted(LESSONS.keys()):
        words = LESSONS[lesson_num]
        parts.append(f"## 第{lesson_num}課\n\n")
        parts.append(TABLE_HEADER)
        for jp, reading, pos, zh in words:
            parts.append(f"| {jp} | {reading} | {pos} | {zh} |\n")
        parts.append("\n")
    return "".join(parts)


